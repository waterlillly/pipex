#include "../../libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	x;

	x = 0;
	if (n == 0)
		return (0);
	while (s1[x] == s2[x] && x < n - 1 && s1[x] != '\0')
		x++;
	return ((unsigned char)s1[x] - (unsigned char)s2[x]);
}

/*
NOTES:
Avoiding sign extension issues:
In C, char can be signed or unsigned depending on the compiler and platform.
If char is signed (which it often is), characters with values above 127
(like extended ASCII characters) would be interpreted as negative numbers.
*/