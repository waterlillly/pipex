#include "../../libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;
	t_list	*tmp2;

	if (!lst || !del)
		return ;
	tmp = *lst;
	*lst = NULL;
	while (tmp)
	{
		tmp2 = tmp;
		(*del)(tmp2->content);
		tmp = tmp->next;
		free(tmp2);
	}
}
